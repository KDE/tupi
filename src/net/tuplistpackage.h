#ifndef TUPLISTPACKAGE_H
#define TUPLISTPACKAGE_H

#include <QDomDocument>

/**
 * @author Jorge Cuadrado
*/

class TupListPackage : public QDomDocument
{
    public:
        enum Type {
            Project = 0x01,
            Files
        };
        
        TupListPackage(Type type, const QString &pattern, bool  regexp = false, bool caseSensitive= false);
        ~TupListPackage();
        
        void setPattern(const QString & pattern);
        void setType(int);
        void setCaseSensitive(bool caseSensitive);
        void setRegexp(bool regexp);
        
    private:
        QDomElement m_caseSensitive, m_options, m_regexp;
};

#endif
